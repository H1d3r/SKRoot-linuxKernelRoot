#pragma once
#include <iostream>
namespace kernel_module {
/***************************************************************************
 * 获取当前Linux内核版本号（三段数字）
 * 
 * 返回格式为 “主.次.补丁” 的版本号字符串，
 * 例如 "5.10.82"。
 * 若获取失败则返回空字符串。
 ***************************************************************************/
std::string get_kernel_version();

/***************************************************************************
 * 判断当前Linux内核版本号是否小于指定版本号
 *
 * 例如：当前版本 "5.10.82" < "5.11.0" 时返回 true，
 *       当前版本 "5.10.82" ≥ "5.10.80" 时返回 false。
 *
 * 参数: ver — 要比较的版本号，如 "5.11.0"
 * 返回:
 *   true  — 当前版本号严格小于 ver
 *   false — 否则
 ***************************************************************************/
bool is_kernel_version_less(const char* ver);
}